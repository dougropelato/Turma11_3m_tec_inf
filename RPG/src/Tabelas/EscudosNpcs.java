public class EscudosNpcs {
    int codigo_escudo;
    int codigo_npc;
    int quantidade_escudos;

    public int getCodigo_escudo() {
        return codigo_escudo;
    }

    public void setCodigo_escudo(int codigo_escudo) {
        this.codigo_escudo = codigo_escudo;
    }

    public int getCodigo_npc() {
        return codigo_npc;
    }

    public void setCodigo_npc(int codigo_npc) {
        this.codigo_npc = codigo_npc;
    }

    public int getQuantidade_escudos() {
        return quantidade_escudos;
    }

    public void setQuantidade_escudos(int quantidade_escudos) {
        this.quantidade_escudos = quantidade_escudos;
    }
    
}
