public class Caminhos {
    int codigo_camino;
    String nome_caminho;
    int codigo_missao;
    int codigo_mapa;

    public int getCodigo_camino() {
        return codigo_camino;
    }

    public void setCodigo_camino(int codigo_camino) {
        this.codigo_camino = codigo_camino;
    }

    public String getNome_caminho() {
        return nome_caminho;
    }

    public void setNome_caminho(String nome_caminho) {
        this.nome_caminho = nome_caminho;
    }

    public int getCodigo_missao() {
        return codigo_missao;
    }

    public void setCodigo_missao(int codigo_missao) {
        this.codigo_missao = codigo_missao;
    }

    public int getCodigo_mapa() {
        return codigo_mapa;
    }

    public void setCodigo_mapa(int codigo_mapa) {
        this.codigo_mapa = codigo_mapa;
    }
    
}
