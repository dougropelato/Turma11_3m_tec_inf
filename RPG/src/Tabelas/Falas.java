package Tabelas;

/**
 *
 * @author Leonardo
 */
public class Falas {
    int codigo_fala;
    String descricao_fala;
    String resposta_negativo_fala;
    String resposta_positivo_fala;

    public int getCodigo_fala() {
        return codigo_fala;
    }

    public void setCodigo_fala(int codigo_fala) {
        this.codigo_fala = codigo_fala;
    }

    public String getDescricao_fala() {
        return descricao_fala;
    }

    public void setDescricao_fala(String descricao_fala) {
        this.descricao_fala = descricao_fala;
    }

    public String getResposta_negativo_fala() {
        return resposta_negativo_fala;
    }

    public void setResposta_negativo_fala(String resposta_negativo_fala) {
        this.resposta_negativo_fala = resposta_negativo_fala;
    }

    public String getResposta_positivo_fala() {
        return resposta_positivo_fala;
    }

    public void setResposta_positivo_fala(String resposta_positivo_fala) {
        this.resposta_positivo_fala = resposta_positivo_fala;
    }
    
}
